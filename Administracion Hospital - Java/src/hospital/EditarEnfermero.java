    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        crudSqlEnfermero objcrud=new crudSqlEnfermero();
        Enfermero objEnfermero=new Enfermero();
        
        objEnfermero.setCedula(txtCedula.getText());
        objEnfermero.setNombre(txtNombre.getText());
        objEnfermero.setApellido(txtApellido.getText());
        objEnfermero.setTipContrato(txtContrato.getText());
        objEnfermero.setSueldo(Float.valueOf(txtSueldo.getText()));
        objcrud.actualizar(objEnfermero.getNombre(), objEnfermero.getApellido(), objEnfermero.getTipContrato(), objEnfermero.getSueldo(), objEnfermero.getCedula());
        txtNombre.setText("");
        txtApellido.setText("");
        txtContrato.setText("");
        txtSueldo.setText("");
    }//GEN-LAST:event_btnActualizarActionPerformed

