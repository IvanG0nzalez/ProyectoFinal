    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        crudSqlEnfermero objcrud=new crudSqlEnfermero();
        Enfermero objEnfermero=new Enfermero();
        
        objEnfermero.setCedula(txtCedula.getText());
        
        objcrud.eliminar(objEnfermero.getCedula());
        txtCedula.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtContrato.setText("");
        txtSueldo.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

