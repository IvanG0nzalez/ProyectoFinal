    private void btnAsignar8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignar8ActionPerformed
        crudSqlEnfermero objcrud=new crudSqlEnfermero();
        Enfermero objEnfermero=new Enfermero();
        
        objEnfermero.setCedula(txtCedEnfermero.getText());
        objEnfermero.setAsignacion(txtPaciente8.getText());
        
        objcrud.asignarPaciente(objEnfermero.getAsignacion(), objEnfermero.getCedula());
        txtPaciente8.setText("");
    }//GEN-LAST:event_btnAsignar8ActionPerformed

